# Definisikan fungsi-fungsi
def multiply_by_two(num):
    return num * 2

def add_numbers(num1, num2):
    return num1 + num2

def square_number(num):
    return num ** 2

# Gunakan fungsi-fungsi tersebut
result1 = multiply_by_two(4)
result2 = add_numbers(result1, 3)
result3 = square_number(result2)

print(result3) # output: 169
